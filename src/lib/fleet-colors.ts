// --- 3. Color Resolution Utility ---
export function resolveFleetColor(semanticName: string, theme: 'light' | 'dark' = 'dark'): string {
  const paletteKey = fleetSemanticColors[theme][semanticName];
  if (!paletteKey) return '#FF00FF'; // Magenta for missing
  return fleetPalette[paletteKey] || '#FF00FF';
}

// --- 4. CSS Variable Generation Utility ---
/**
 * Generates CSS variable declarations for all Fleet semantic colors for both light and dark themes.
 * Returns an object: { light: string, dark: string }
 */
export function generateFleetCssVariables() {
  const toVars = (theme: 'light' | 'dark') => {
    return Object.entries(fleetSemanticColors[theme])
      .map(([semantic, paletteKey]) => {
        const cssVar = `--fleet-${semantic.replace(/\./g, '-')}`;
        const hex = fleetPalette[paletteKey] || '#FF00FF';
        return `  ${cssVar}: ${hex};`;
      })
      .join('\n');
  };
  return {
    light: toVars('light'),
    dark: toVars('dark'),
  };
} 